    private void btnbackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbackActionPerformed
String backSpace=null;
    if(VisorOP.getText().length()>0)
    {
        StringBuilder str=new StringBuilder(VisorOP.getText());
        str.deleteCharAt(VisorOP.getText().length()-1);
        backSpace=str.toString();
        VisorOP.setText(backSpace);
    }
    if(Visor.getText().length()>0)
    {
        StringBuilder str=new StringBuilder(Visor.getText());
        str.deleteCharAt(Visor.getText().length()-1);
        backSpace=str.toString();
        Visor.setText(backSpace);
    }
    if(Visor.getText().isEmpty()){
        Visor.setText("0");}
    if(VisorOP.getText().isEmpty()){
        VisorOP.setText("0");}
    btnvir.setEnabled(true);
    

    }//GEN-LAST:event_btnbackActionPerformed


    private void jButton26ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton26ActionPerformed
valor2=Double.parseDouble(Visor.getText());
if(operacao=="+")
{
    resultado=valor1+valor2;
    ans=String.format("%.2f", resultado);
    Visor.setText(ans);
}
else if(operacao=="-")
{
    resultado=valor1-valor2;
    ans=String.format("%.2f", resultado);
    Visor.setText(ans);
}
else if(operacao=="*")
{
    resultado=valor1*valor2;
    ans=String.format("%.2f", resultado);
    Visor.setText(ans);
}
else if(operacao=="/")
{
    resultado=valor1/valor2;
    ans=String.format("%.2f", resultado);
    Visor.setText(ans);
}
else if(operacao=="%")
{
    resultado=valor1*valor2/100;
    ans=String.format("%.2f", resultado);
    Visor.setText(ans);
}
else if(operacao=="x^y")
{
    double resultadoo=1;
    for(int i=0;i<valor2;i++)
    {
    resultadoo=valor1*resultadoo;
    }
    ans=String.format("%.2f", resultadoo);
    Visor.setText(ans);
 }
addNum("=");
btnvir.setEnabled(true);



    }//GEN-LAST:event_jButton26ActionPerformed

